// Solve the HYPERBOLIC Kepler equation M = e*sinh(H) - H for H.
function solveKeplerHyperbolic(M, e) {
  let H = Math.asinh(M / e);                 // good first guess
  for (let i = 0; i < 30; i++) {
    const f = e * Math.sinh(H) - H - M;
    H = H - f / (e * Math.cosh(H) - 1);
    if (Math.abs(f) < 1e-13) break;
  }
  return H;
}

