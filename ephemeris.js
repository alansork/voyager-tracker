// Read a position out of a table (au), clamped to the table's ends.
function tablePositionAu(t, jdTdb) {
  const n = t.xyz.length / 3;
  let f = (jdTdb - t.startJd) / t.stepDays;
  f = Math.min(n - 1, Math.max(0, f));
  const i = Math.min(n - 2, Math.floor(f));
  const u = f - i;                       // 0..1 between sample i and i+1
  const a = i * 3, b = (i + 1) * 3;
  return {
    x: t.xyz[a] + (t.xyz[b] - t.xyz[a]) * u,
    y: t.xyz[a + 1] + (t.xyz[b + 1] - t.xyz[a + 1]) * u,
    z: t.xyz[a + 2] + (t.xyz[b + 2] - t.xyz[a + 2]) * u,
  };
}
function tableEndJd(t) { return t.startJd + (t.xyz.length / 3 - 1) * t.stepDays; }

function voyager1EarlyPositionAu(jdTdb) { return tablePositionAu(VOYAGER1_EARLY, jdTdb); }
const VOYAGER1_TABLE_END_JD = tableEndJd(VOYAGER1_CRUISE);   // 2026-07-15

// Voyager 1's heliocentric position (au, ecliptic J2000) at a TDB Julian Day.
function voyager1HyperbolicPositionAu(jdTdb) {
  const M = VOYAGER1.n * (jdTdb - VOYAGER1.tpJd) * DEG;
  const H = solveKeplerHyperbolic(M, VOYAGER1.e);
  const a = VOYAGER1.a, e = VOYAGER1.e;
  const xp = a * (Math.cosh(H) - e);                      // note: cosh, not cos
  const yp = -a * Math.sqrt(e * e - 1) * Math.sinh(H);
  return orbitPlaneToEcliptic(
    xp, yp, VOYAGER1.peri * DEG, VOYAGER1.node * DEG, VOYAGER1.i * DEG
  );
}

// The one function the app calls: real position at ANY moment of the
// mission. Daily table through the flyby years, 10-day table through the
// quiet decades, escape hyperbola from 2026 onward. Every era is NASA's
// tracked trajectory — nothing is invented.
function voyager1PositionAu(jdTdb) {
  if (jdTdb <= tableEndJd(VOYAGER1_EARLY)) return tablePositionAu(VOYAGER1_EARLY, jdTdb);
  if (jdTdb <= VOYAGER1_TABLE_END_JD) return tablePositionAu(VOYAGER1_CRUISE, jdTdb);
  return voyager1HyperbolicPositionAu(jdTdb);
}

// Voyager 1's speed (km/s). On the final hyperbola we use the vis-viva
// equation, the energy law of orbits: v² = GM_sun * (2/r - 1/a). In the
// early years (different orbit between each flyby) we simply measure how
// far the tracked table moves in half a day either side.
const GM_SUN_AU3_DAY2 = 2.9591220828559093e-4;   // Sun's gravity, au³/day²
function voyager1SpeedKmS(jdTdb) {
  if (jdTdb <= VOYAGER1_TABLE_END_JD) {
    const p0 = voyager1PositionAu(jdTdb - 0.5);
    const p1 = voyager1PositionAu(jdTdb + 0.5);
    return distanceAu(p0, p1) * AU_KM / DAY_S;   // au per day -> km/s
  }
  const p = voyager1PositionAu(jdTdb);
  const r = Math.hypot(p.x, p.y, p.z);
  const v2 = GM_SUN_AU3_DAY2 * (2 / r - 1 / VOYAGER1.a);  // (au/day)²
  return Math.sqrt(v2) * AU_KM / DAY_S;
}

// --- Readouts the app shows -------------------------------------------------

function distanceAu(p, q) {
  return Math.hypot(p.x - q.x, p.y - q.y, p.z - q.z);
}

// Everything about Voyager 1 at one moment, ready for display.
function voyager1Status(date) {
  const jd = jdTdbFromDate(date);
  const v = voyager1PositionAu(jd);
  const e = planetPositionAu(PLANETS.earth, jd);
  const fromSunAu = Math.hypot(v.x, v.y, v.z);
  const fromEarthAu = distanceAu(v, e);
  const lightSeconds = fromEarthAu * LIGHT_SEC_PER_AU;
  return {
    positionAu: v,
    fromSunAu,
    fromSunKm: fromSunAu * AU_KM,
    fromEarthAu,
    lightHoursFromEarth: lightSeconds / 3600,   // radio takes this long, one way
    speedKmS: voyager1SpeedKmS(jd),
    missionYears: (date.getTime() - VOYAGER1.launched) / (365.25 * DAY_S * 1000),
  };
}

